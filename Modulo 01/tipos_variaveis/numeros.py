# Inteiro
numero_inteiro = 4
print("Inteiro = ", numero_inteiro)

# Real com ponto flutuante
numero_real = 3.14
print("Real com ponto flutuante = ", numero_real)

# type()
print("Tipo da variável numero_inteiro = ", type(numero_inteiro))
print("Tipo da variável real = ", type(numero_real))

# soma +
soma = 1 + 1
print("Soma = ", soma)

# subtração -
subtracao = 1 - 1
print("Subtração = ", subtracao)

# multiplicação *
multiplicacao = 1 * 3
print("Multiplicação = ")

# divisao /
divisao = 5 / 2
print("Divisao = ", divisao)
print("Tipo da variável do resultado da divisao = ", type(divisao))

# int() converte um valor para inteiro
print("Valor em inteiro = ", int(divisao ))

# float () converte um valor para float
print("Valor em float = ", float(soma))

# Modulo % é o resto da divisão
modulo = 5 % 2
print("Modulo = ", modulo)

# divisão inteiro //
divisao_inteiro = 5 // 2
print("Tipo de variável do resultado da divisão = ", type(divisao_inteiro))